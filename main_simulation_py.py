num_elements = 16
frequency = 28e9  # 28 GHz
lambda_w = 3e8 / frequency
spacing = lambda_w / 2

# Grid for Wave Propagation Simulation
res = 150
X, Y = np.meshgrid(np.linspace(0, 10, res), np.linspace(0, 10, res))

# 2. Hybrid Physics-Protocol Engine
def get_hybrid_field(target_ue_pos, elements, space, wav):
    """
    Calculates the actual electromagnetic interference pattern (Beamforming)
    based on constructive and destructive interference.
    """
    dx_u = target_ue_pos[0] - ris_pos[0]
    dy_u = target_ue_pos[1] - ris_pos[1]
    theta_d = np.arctan2(dy_u, dx_u)

    # AI-Calculated Phase Shifts to steer the beam
    phases = -2 * np.pi * (np.arange(elements) * space / wav) * np.cos(theta_d)

    complex_field = np.zeros(X.shape, dtype=complex)
    for i in range(elements):
        # Physical offset for each antenna element
        element_x = ris_pos[0] + (i - elements/2) * space
        r = np.sqrt((X - element_x)**2 + (Y - ris_pos[1])**2)
        # Superposition of waves
        complex_field += (1/r) * np.exp(1j * (phases[i] + 2 * np.pi * r / wav))

    power = 20 * np.log10(np.abs(complex_field))
    return power - np.max(power) # Normalized dB Map

