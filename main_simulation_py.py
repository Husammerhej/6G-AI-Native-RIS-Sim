def calculate_ris_beamforming_gain(elements_count):
    """
    Calculates the theoretical power gain provided by the RIS based on the number of elements.
    Principle: In ideal beamforming, the gain is proportional to the square of the
    number of elements (N^2).
    """
    N = elements_count
    # Gain in dB = 20 * log10(N)
    # Reflects the coherent combining of signals at the receiver
    gain_db = 20 * np.log10(N)
    return gain_db

