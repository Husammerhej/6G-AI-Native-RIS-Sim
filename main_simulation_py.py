def update_6g_performance_simulation(ue_x, ue_y, num_elements):
    """
    Real-time 6G Performance Analysis Tool.
    Evaluates the impact of RIS surface size and user location on
    Signal Strength (dBm) and Achievable Data Rates (Gbps).
    """
    # 1. Spatial Configuration
    ris_pos = np.array([5, 0])
    target_ue = np.array([ue_x, ue_y])

    # 2. Geometric Beam Steering Calculation
    dx = target_ue[0] - ris_pos[0]
    dy = target_ue[1] - ris_pos[1]
    steering_angle = np.degrees(np.arctan2(dy, dx))

    # 3. Link Budget & Performance Metrics
    # RSSI calculation based on RIS Gain (proportional to N^2) and Path Loss
    distance = np.sqrt(dx**2 + dy**2)

    # Gain calculation (N^2 principle for coherent beamforming)
    ris_gain = 10 * np.log10(num_elements**2)

    # Simplified Free Space Path Loss (FSPL) model
    path_loss = 20 * np.log10(distance + 1)

    # Estimated Received Signal Strength Indicator (RSSI)
    rssi = ris_gain - path_loss + 20 # Normalized baseline

    # Theoretical 6G Throughput (Gbps) estimation
    # Mapping signal quality to Shannon capacity-inspired metrics
    throughput_gbps = max(0, (rssi + 50) * 0.1)

    # 4. Data Visualization
    plt.figure(figsize=(14, 6))

    # Subplot A: Spatial Environment & Beam Alignment
    plt.subplot(1, 2, 1)
    plt.xlim(0, 10); plt.ylim(-1, 10)
    plt.scatter(ris_pos[0], ris_pos[1], color='gold', s=400, marker='s', label='RIS Controller')
    plt.scatter(target_ue[0], target_ue[1], color='green', s=150, marker='o', label='Mobile UE')

    # Visualize beamwidth based on element count
    plt.plot([ris_pos[0], target_ue[0]], [ris_pos[1], target_ue[1]],
             'orange', linewidth=num_elements/64, alpha=0.6)

    plt.title(f"Spatial Alignment | Angle: {steering_angle:.1f}°")
    plt.xlabel("Width (m)")
    plt.ylabel("Distance (m)")
    plt.legend(loc='upper right')
    plt.grid(True, alpha=0.2)

    # Subplot B: Performance Analytics (KPIs)
    plt.subplot(1, 2, 2)
    kpis = ['Signal (dBm)', 'Throughput (Gbps)']
    values = [rssi, throughput_gbps]
    colors = ['#1f77b4', '#2ca02c'] # Professional color palette

    plt.bar(kpis, values, color=colors)
    plt.ylim(0, 100 if rssi > 0 else 10)
    plt.title("Real-time 6G KPI Analytics")
    plt.ylabel("Calculated Metrics")

    plt.tight_layout()
    plt.show()

