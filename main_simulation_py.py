def calculate_beam_steering_angle(ris_coord, target_coord):
    """
    Calculates the required Phase Shift/Steering Angle to focus the beam.
    Uses Trigonometric functions to determine the Angle of Departure (AoD).
    """
    dy = target_coord[1] - ris_coord[1]
    dx = target_coord[0] - ris_coord[0]
    angle = np.degrees(np.arctan2(dy, dx))
    return angle

